    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            URL url = new URL("http://localhost:8080/api/patient");
            HttpURLConnection con = (HttpURLConnection) url.openConnection();
            con.setRequestMethod("GET");

            int responseCode = con.getResponseCode();
            if (responseCode == HttpURLConnection.HTTP_OK) {
                BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                String inputLine;
                StringBuilder response = new StringBuilder();

                while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
                }
                in.close();

                JSONArray jsonArray = new JSONArray(response.toString());
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                for (int i = 0; i < jsonArray.length(); i++) {
                    JSONObject jsonObject = jsonArray.getJSONObject(i);
                    String dni = jsonObject.getString("dni");
                    String name = jsonObject.getString("name");
                    String lastname = jsonObject.getString("lastname");
                    String bornDate = jsonObject.getString("bornDate").substring(0, 10); // Formato AÑO-MES-DIA
                    String phone = jsonObject.getString("phone");
                    String gender = jsonObject.getString("gender");

                    model.addRow(new Object[]{dni, name, lastname, bornDate, phone, gender});
                }
            } else {
                System.out.println("Error en la conexión: " + responseCode);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

