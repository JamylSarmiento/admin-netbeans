    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Obtener los valores de los campos de texto y el combobox
        String dni = jTextField1.getText();
        String name = jTextField2.getText();
        String lastname = jTextField3.getText();
        String bornDate = jTextField4.getText(); // Considerando que ya está en el formato Año-Mes-Día
        String phone = jTextField5.getText();
        String gender = "";
            switch(jComboBox1.getSelectedIndex()) {
                case 0: gender = "M"; break; // Med. General
                case 1: gender = "F"; break; // Pediatria
                default: break;
            }
        

        // Crea el objeto JSON
        JSONObject json = new JSONObject();
        json.put("dni", dni);
        json.put("name", name);
        json.put("lastname", lastname);
        json.put("bornDate", bornDate);
        json.put("phone", phone);
        json.put("gender", gender);

        // Realizar la petición POST
        try {
            // Establecer la URL del endpoint
            URL url = new URL("http://localhost:8080/api/patient");

            // Abrir la conexión
            HttpURLConnection con = (HttpURLConnection) url.openConnection();

            // Especificar que la conexión permitirá el envío de datos
            con.setDoOutput(true);

            // Establecer el método de la petición
            con.setRequestMethod("POST");

            // Establecer el tipo de contenido del cuerpo de la petición
            con.setRequestProperty("Content-Type", "application/json");

            // Obtener el flujo de salida para escribir el cuerpo de la petición
            OutputStream os = con.getOutputStream();
            os.write(json.toString().getBytes());
            os.flush();
            os.close();

            // Leer la respuesta del servidor
            BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
            String inputLine;
            StringBuffer response = new StringBuffer();
            while ((inputLine = in.readLine()) != null) {
                response.append(inputLine);
            }
            JOptionPane.showMessageDialog(this, "Paciente registrado exitosamente");
            in.close();

            // Imprimir la respuesta del servidor
            System.out.println(response.toString());
            
            // Cerrar la conexión
            con.disconnect();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

